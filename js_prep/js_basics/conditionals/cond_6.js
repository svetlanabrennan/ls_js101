let weather = "stormy";

switch (weather) {
  case "sunny":
    console.log("It's a beautiful day!");
    break;
  case "rainy":
    console.log("Grab your umbrella.");
    break;
  case "stomry":
    console.log("Dress warm.");
    break;
  default:
    console.log("Let's stay inside.");
    break;
}